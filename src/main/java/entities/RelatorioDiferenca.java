package entities;

public class RelatorioDiferenca {
	public Divergencia dra;
	public Divergencia dr;
	public Divergencia fc;
	public Divergencia bpp;
	public Divergencia bpa;
	public Divergencia dva;
	
	
	public Divergencia getDra() {
		return dra;
	}
	public void setDra(Divergencia dra) {
		this.dra = dra;
	}
	public Divergencia getDr() {
		return dr;
	}
	public void setDr(Divergencia dr) {
		this.dr = dr;
	}
	public Divergencia getFc() {
		return fc;
	}
	public void setFc(Divergencia fc) {
		this.fc = fc;
	}
	public Divergencia getBpp() {
		return bpp;
	}
	public void setBpp(Divergencia bpp) {
		this.bpp = bpp;
	}
	public Divergencia getBpa() {
		return bpa;
	}
	public void setBpa(Divergencia bpa) {
		this.bpa = bpa;
	}
	public Divergencia getDva() {
		return dva;
	}
	public void setDva(Divergencia dva) {
		this.dva = dva;
	}
}
